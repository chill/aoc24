package main

import (
	"testing"

	"aoc24/lib"
)

func TestCalc(t *testing.T) {
	const (
		in = `............
........0...
.....0......
.......0....
....0.......
......A.....
............
............
........A...
.........A..
............
............`

		expect = 14
	)

	if res := calc(lib.StringLines(in)); res != expect {
		t.Errorf("expected %d got %d", expect, res)
	}
}
